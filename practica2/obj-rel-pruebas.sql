FROM CuentaAhorro
WHERE Saldo > 1000 
ORDER BY Saldo DESC 
FETCH FIRST 10 ROWS ONLY; 

--Consulta para obtener el saldo total de todas las cuentas corrientes en una oficina específica
SELECT SUM(Saldo)
FROM CuentaCorriente
WHERE Oficina = OficinaUDT(2);


--Consulta para obtener el nombre apellidos de las personas que tienen una cuenta corriente y son menores de 70 años
SELECT c.Nombre, c.Apellidos, c.Edad, cu.IBAN
FROM CuentaCorriente cu, Cliente c
WHERE cu.oid IN (SELECT IBAN FROM Posee WHERE DNI = c.oid) AND c.Edad < 70;


--Consulta para obtener el número total de operaciones realizadas por cada cliente:
SELECT c.Nombre, c.Apellidos, COUNT(*) AS total_operaciones
FROM Cliente c
JOIN Posee p ON c.oid=p.DNI
JOIN Cuenta cu ON cu.oid =p.IBAN
JOIN Operacion o ON cu.oid=o.CuentaOrigen
GROUP BY c.Nombre, c.Apellidos;


